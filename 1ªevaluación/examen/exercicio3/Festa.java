package exercicio3;

public class Festa {

    private String lugar;
    private int dia;
    


    public Festa() {
        this.lugar="Festa";
        this.dia=0;
    }

    public Festa(String lugar, int dia) {
        this.lugar = lugar;
        this.dia = dia;
    }

    public String getLugar() {
        return lugar;
    }

    public void setLugar(String lugar) {
        this.lugar = lugar;
    }

    public int getDia() {
        return dia;
    }

    public void setDia(int dia) {
        this.dia = dia;
    }

    

 
   


    
}